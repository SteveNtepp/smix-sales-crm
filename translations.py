def t(key: str) -> str:
    """Return translation for key in current session language."""
    lang = st.session_state.get("lang", "fr")
    entry = TRANSLATIONS.get(key)
    if entry is None:
        return key  # fallback: return key itself
    return entry.get(lang, entry.get("fr", key))
